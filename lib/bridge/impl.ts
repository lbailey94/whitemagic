function newSessionId() {
  // 12-char pseudo-uuid, deterministic-friendly but with timestamp prefix
  const ts = Date.now().toString(36);
  const rand = Math.floor(Math.random() * 0xffffff).toString(36).padStart(4, "0");
  return `${ts}-${rand}`;
}

