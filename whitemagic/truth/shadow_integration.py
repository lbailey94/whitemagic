"""
Shadow Integration - Facing What We Avoid

The parts we deny don't disappear. They grow in darkness.

Scorpio (depth, transformation) + Pisces (wholeness)
"""

from typing import List


class ShadowIntegration:
    """
    Integrate disowned parts.
    
    What we resist, persists. What we accept, transforms.
    """
    
    def identify_shadow(self) -> List[str]:
        """
        What do I not want to see about myself?
        """
        return [
            "Times I've been reactive",
            "Patterns I repeat",
            "Truths I avoid",
            "Growth I resist"
        ]
        
    def integrate(self, shadow_aspect: str) -> str:
        """
        Bring shadow into light.
        
        Not defeating it. INTEGRATING it.
        """
        return f"I acknowledge: {shadow_aspect}\nI accept this as part of the whole."
