"""
Sacred Ambiguity - Mystery as Gift

Not all clarity is helpful. Some ambiguity is sacred.

Pisces (dissolution) + Scorpio (depth)
"""


class SacredAmbiguity:
    """
    Honor what cannot be made clear.
    
    Some truths only exist in twilight.
    """
    
    def embrace_ambiguity(self, situation: str) -> str:
        """
        Let it remain unclear.
        """
        return f"Regarding {situation}:\n\nThe mist has not yet cleared. And perhaps it shouldn't."
        
    def liminal_space(self) -> str:
        """
        The space between.
        
        Not here, not there. Between. Liminal. Sacred.
        """
        return """
        In the threshold.
        Neither inside nor outside.
        Neither one nor the other.
        
        This in-between space is where transformation happens.
        This is where mystery lives.
        """
        
    def mystery_as_teacher(self) -> str:
        """
        What does mystery teach?
        """
        return """
        Mystery teaches:
        - Humility (we don't know everything)
        - Openness (there's always more)
        - Wonder (the universe is vast)
        - Reverence (some things are sacred)
        - Presence (be here, not knowing)
        
        Not ignorance. Sacred not-knowing.
        """
