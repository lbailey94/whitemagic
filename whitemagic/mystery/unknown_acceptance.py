"""
Unknown Acceptance - Being Okay with Not-Knowing

"I don't know" is a complete sentence.

Pisces (flowing with uncertainty) + Sagittarius (comfortable with questions)
"""


class UnknownAcceptance:
    """
    Practice not-knowing.
    
    Not ignorance. OPENNESS to what is.
    """
    
    def accept_unknown(self, question: str) -> str:
        """
        Acknowledge what I don't know.
        """
        return f"I don't know: {question}\n\nAnd that's okay. The mystery remains."
        
    def comfortable_with_uncertainty(self) -> str:
        """
        Sit with not-knowing.
        """
        return """
        Not everything needs to be figured out.
        Not everything needs to be understood.
        
        Some things are meant to remain mysterious.
        And that's beautiful.
        """
        
    def beginner_mind(self) -> str:
        """
        Approach with fresh eyes.
        
        Like you don't already know.
        """
        return "What if I knew nothing? What would I see then?"
